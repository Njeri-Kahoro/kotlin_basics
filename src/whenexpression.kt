fun main(args: Array<String>) {
    var ch= 'K'
    when(ch){
        'A'-> println("A is vowel")
        'E' -> println("E is vowel")
        'I' -> println("I is vowel")
        'O' -> println("O is vowel")
        'u' -> println("U is vowel")

        else -> println("$ch is a consonant")
    }
}